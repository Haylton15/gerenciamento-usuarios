class User{
    
    constructor(name, gender, birth, country, email, password, photo, admin){
        //this aqui uma vez instanciado um objeto da classe User o this se refere a ele mesmo
        this.name = name;
        this.gender = gender;
        this.birth = birth;
        this.country = country;
        this.email = email;
        this.password = password;
        this.photo = photo;
        this.admin = admin;

    }


}